	public void commonCode1(int originTile, int destinationTile){
		GameBoard gb = new GameBoard(0);
		gb.populateStaticMoveableTileArray(staticMoveableTileArray1());
		gb.populateBoardWithFixedTiles();
		gb.populateBoardWithMoveableTiles();
		Player p = new Player("Red");
		p.setGameBoard(gb);
		
		//we place player p at the originTile
		int[] coord = gb.getCoordinates(gb.getTileFromTileNumber(originTile));
		gb.getBoard()[coord[0]][coord[1]].addPlayer(p);
		p.setCurrentTile(gb.getTileFromTileNumber(originTile));
		
		//if b is true, it means that checkIfMoveLegal has said the player can move from origin to destination
		//if b false, it means checkIfMoveLegal has said the player can't move from origin to destination
		boolean b = gb.checkIfMoveLegal(gb.getTileFromTileNumber(originTile), gb.getTileFromTileNumber(destinationTile));
		
		AbstractTile expectedDestination;
		//if b==true then we expect player to have moved to destination tile AFTER the
		//call to moveToTile
		if(b==true){
			expectedDestination = gb.getTileFromTileNumber(destinationTile);
		}
		//if b==false, we expect player's position AFTER the call to moveToTile to be
		//the same as before the call (remain origin tile)
		else{
			expectedDestination = gb.getTileFromTileNumber(originTile);
		}

		//call to moveToTile
		p.moveToTile(destinationTile);
		
		AbstractTile actualDestination = p.getCurrentTile();
		
		//gb.printBoard();
		
		assertTrue("", (expectedDestination == actualDestination));
	}
	
